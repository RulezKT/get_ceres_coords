# v2024
# get coords x , y , z from ceres_type2.bsp
def get_coords_type2_ceres(date_in_seconds: int, file_in_mem: bytes) -> Position:
    # target_code = 20000001
    # center_code =10
    # ref_frame = 1
    # type_of_data = 2
    # ND =  2
    # NI =  6
    if START_TIME >= date_in_seconds or LAST_TIME <= date_in_seconds:
        print("get_coords: Date is out of range")
        return {"x": 0, "y": 0, "z": 0}

    for i in range(1, len(CERES_FILE_RECORDS)):
        if (
            CERES_FILE_RECORDS[i]["segment_start_time"] <= date_in_seconds
            and CERES_FILE_RECORDS[i]["segment_last_time"] > date_in_seconds
        ):
            start_addr = CERES_FILE_RECORDS[i]["rec_start_addr"]
            info_addr = (CERES_FILE_RECORDS[i]["record_last_adress"] - 4) * 8
            # big endian
            init, intlen, rsize, n = struct.unpack(
                ">4d", file_in_mem[info_addr : info_addr + 32]
            )
            break
    # print(date_in_seconds)
    # print(CERES_FILE_RECORDS[i]["segment_start_time"])
    internal_offset = (
        math.floor(
            (date_in_seconds - CERES_FILE_RECORDS[i]["segment_start_time"]) / intlen
        )
        * rsize
    )
    # print(internal_offset)
    record = 8 * int(start_addr + internal_offset)

    # the only place, we read data from bsp file, loaded in memory
    # data = array.array("d", file_in_mem[record - 8 : record + int(rsize) * 8])
    fmt = f">{int(rsize+1)}d"
    # print(fmt)
    data = struct.unpack(fmt, file_in_mem[record - 8 : record + int(rsize) * 8])

    tau = (date_in_seconds - data[0]) / data[1]

    order = int((rsize - 2) / 3) - 1
    deg = order + 1

    return {
        "x": _chebyshev(order, tau, data[2 : 2 + deg]),
        "y": _chebyshev(order, tau, data[2 + deg : 2 + 2 * deg]),
        "z": _chebyshev(order, tau, data[2 + 2 * deg : 2 + 3 * deg]),
    }


def _chebyshev(order: int, x: float, data: list[float]) -> float:
    # Evaluate a Chebyshev polynomial
    two_x = 2 * x
    bkp2 = data[order]
    bkp1 = two_x * bkp2 + data[order - 1]
    for n in range(order - 2, 0, -1):
        bk = data[n] + two_x * bkp1 - bkp2
        bkp2 = bkp1
        bkp1 = bk

    return data[0] + x * bkp1 - bkp2
